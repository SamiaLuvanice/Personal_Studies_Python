# 1 - Busra os dois primeiros elementos da lista
gamesList = ["Fifa23", "GTA5", "Minecraft", "Valorant", "Fortnite"]
print(gamesList[:2])  # Acessando os dois primeiros elementos

# 2 - Buscar o último item da lista
print(gamesList[-1])  # Acessando o último elemento da lista

# 3 - Buscar jogos até uma determinada posição
print(gamesList[:3])  # Acessando os jogos até a posição 3 (exclusivo)

# 4 - Buscar jogos a partir de uma determinada posição
print(gamesList[2:])  # Acessando os jogos a partir da posição 2 (inclusive)